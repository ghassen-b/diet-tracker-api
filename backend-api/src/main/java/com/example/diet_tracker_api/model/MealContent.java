package com.example.diet_tracker_api.model;

/**
 * Enum of supported meal contents.
 */
public enum MealContent {
    /**
     * Meal entierly vegan (no animal products).
     */
    VEGAN,

    /**
     * Meal entierly vegetarian (no meat).
     */
    VEGETARIAN,

    /**
     * Fish-based meal.
     */
    FISH,

    /**
     * Chicken-based meal.
     */
    CHICKEN,

    /**
     * Pork-based meal.
     */
    PORK,

    /**
     * Lamb-based meal.
     */
    LAMB,

    /**
     * Beef-based meal.
     */
    BEEF;
}
